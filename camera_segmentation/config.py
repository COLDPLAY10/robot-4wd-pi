"""
Конфигурация нейросетевой сегментации пола (SegFormer-B0, ADE20K).
"""

import os

# Путь к ONNX-модели (экспорт: scripts/setup_segformer_model.py).
# Абсолютный относительно корня репо — иначе ломается при запуске из map_scripts/.
SEGFORMER_MODEL_PATH = os.path.join(
    os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
    'models', 'segformer_b0_ade.onnx',
)

# Размер входа модели (квадрат, как при обучении ade-512-512).
SEGFORMER_INPUT_SIZE = 512

# Индексы класса "пол" в ADE20K — ПРОВЕРЕНЫ через model.config.id2label
# (scripts/setup_segformer_model.py): 3 = 'floor', 6 = 'road', 13 = 'earth'.
#
# Почему не только floor (3): эмпирическая проверка на кадрах заезда
# (scripts/eval_test_pictures.py, террацо/полированный бетон под углом) показала,
# что SegFormer-B0 уверенно зовёт такой пол классами 'road' и 'earth', а не
# 'floor' — на этих покрытиях floor-only давал 0-19% маски там, где физически
# сплошной пол. Добавление 6+13 поднимает их до 90%+. Робот ездит в помещении,
# где 'road'/'earth' физически быть не может, поэтому риск ложного "пола" низкий.
# Классы 'water' (21) и 'grass' (9), куда уходит крапчатый пол на крупных планах,
# НЕ добавляем — они дают ложные срабатывания на реальных бликах/растениях.
# Ковёр (28 = 'rug') по умолчанию НЕ считаем полом — добавьте 28, если робот
# ездит по ковровому покрытию.
FLOOR_CLASS_IDS = (3, 6, 13)